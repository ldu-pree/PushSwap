#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	s;

	i = 0;
	s = 0;
	while (s1[i])
		i++;
	while (*s2)
	{
		s1[i + s] = s2[s];
		if (s2[s] == '\0')
			return (s1);
		s++;
	}
	return (s1);
}
