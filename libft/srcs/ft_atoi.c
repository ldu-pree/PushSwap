#include "libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int	num;
	int	sign;

	i = 0;
	num = 0;
	sign = 1;
	while (ft_iswhitespace(str[i]))
		i++;
	if (*(str + i) == '-')
		sign = -1;
	if (*(str + i) == '-' || *(str + i) == '+')
		i++;
	while (ft_isdigit(str[i]))
	{
		num = num * 10 + (*(str + i++) - '0');
	}
	return (num * sign);
}
